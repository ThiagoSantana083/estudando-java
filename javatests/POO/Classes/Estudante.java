    // Nesse caso estamos fazendo uma classe Estudante com atributos (variáveis) para ser usado depois.
    // Ela é uma estrutura que define as características e comportamentos que os objetos desse tipo terão.
    // Em resumo, as classes no POO são abstrações que nos permitem modelar objetos do mundo real em um programa de
    // computador, fornecendo uma estrutura para organizar e gerenciar o código de forma mais eficiente e reutilizável.
    public int idade;
    public String nome;
    public char sexo;
