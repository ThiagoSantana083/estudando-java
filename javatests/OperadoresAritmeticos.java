package javatests;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        /* Operadores aritmeticos = + -> (soma) | -> (subtração) | / -> (divisão) | * -> (multiplicação) | % -> (resto
         da divisão) */
        double numero01 = 50;
        double numero02 = 20;
        double resultado = numero01 + numero02;

        System.out.println(resultado);
    }
}
